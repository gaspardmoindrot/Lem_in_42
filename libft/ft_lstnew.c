#include "libft.h"

t_list		*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*list;

	list = (t_list *)malloc(sizeof(t_list));
	if (!list)
		return (0);
	if (content == NULL)
	{
		list->content = NULL;
		list->content_size = 0;
		list->next = NULL;
		return (list);
	}
	list->content = malloc(content_size);
	if (!list->content)
		return (0);
	ft_memmove(list->content, content, content_size);
	list->content_size = content_size;
	list->next = NULL;
	return (list);
}
